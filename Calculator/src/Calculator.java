// Add 1,2,3,4,5

public class Calculator {


	public int add(int num1,int num2){
		int result=0;
		result=num1+num2;
		
		return result;
	}
	public int sub( int num1,int num2){
		int result1=0;
		result1=num1-num2;
		return result1;
	}
	public int mul( int num1,int num2){
		int result1=0;
		result1=num1*num2;
		return result1;
	}
	public int div( int num1,int num2){
		int result1=0;
		result1=num1/num2;
		return result1;
	}
}
